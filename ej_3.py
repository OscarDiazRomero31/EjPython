#Calcula el área de un triángulo con la fórmula: Área = (base * altura) / 2.

print("Vamos a calcular el area del triangulo")
base = int(input("Introduce el valor de la base: "))
altura= int(input("Introduce el valor de la altura: "))

formula = ((base * altura) / 2)

print("El area del triangulo es: "+str(formula))